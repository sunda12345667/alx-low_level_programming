#include <stdio.h>
/**
 * add - add two integers
 * @a: first unt
 * @b: second int
 * Returen: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first unt
 * @b: second int
 * Returen: subtracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - division two integers
 * @a: first unt
 * @b: second int
 * Returen: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod of two integers
 * @a: first unt
 * @b: second int
 * Returen: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiplication two integers
 * @a: first unt
 * @b: second int
 * Returen: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
